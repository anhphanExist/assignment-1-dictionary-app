public class Word {
	private String target;
	private String explain;

	//Set + get Target
	public void setTarget(String _target) {
		this.target = _target;
	}
	public String getTarget() {
		return this.target;
	}

	//Set + get Explain
	public void setExplain(String _explain) {
		this.explain = _explain;
	}
	public String getExplain() {
		return this.explain;
	}


}