public class Word {
	private String Target, Explain;

	//Set + get Target
	public void setTarget(String _Target){
		this.Target = _Target;
	}
	public String getTarget(){
		return this.Target;
	}

	//Set + get Explain
	public void setExplain(String _Explain){
		this.Explain = _Explain;
	}
	public String getExplain(){
		return this.Explain;
	}


}